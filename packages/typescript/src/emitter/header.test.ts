import { describe, it, expect } from "vitest";
import {
  buildHeaderVariables,
  DEFAULT_HEADER_FN,
  formatHeader,
  HEADER_VARIABLES,
} from "./header.js";
import type { HeaderVariables } from "./header.js";

describe("formatHeader()", () => {
  it("uses DEFAULT_HEADER_FN when called with undefined", () => {
    const result = formatHeader(undefined);
    expect(result).toContain("This file is generated by ghagen from <unknown>");
    expect(result).toContain("Do not edit manually.");
  });

  it("returns a static string verbatim", () => {
    expect(formatHeader("Custom header text")).toBe("Custom header text");
  });

  it("calls a closure with HeaderVariables", () => {
    const fn = (vars: HeaderVariables) => `Built with ${vars.tool} v${vars.version}`;
    const result = formatHeader(fn);
    expect(result).toMatch(/^Built with ghagen v\d+\.\d+\.\d+$/);
  });

  it("returns null when header is null", () => {
    expect(formatHeader(null)).toBeNull();
  });

  it("passes source location to closure", () => {
    const fn = (vars: HeaderVariables) => `from ${vars.source_file}:${vars.source_line}`;
    const result = formatHeader(fn, {
      file: "/repo/.github/workflows.ts",
      line: 42,
    });
    expect(result).toContain(":42");
    expect(result).toContain("workflows.ts");
  });

  it("does not interpolate placeholders in static strings", () => {
    const result = formatHeader("{source_file} is literal");
    expect(result).toBe("{source_file} is literal");
  });
});

describe("DEFAULT_HEADER_FN", () => {
  it("produces the standard header text", () => {
    const vars: HeaderVariables = {
      source_file: "ci/workflows.ts",
      source_line: "10",
      tool: "ghagen",
      version: "1.0.0",
    };
    const result = DEFAULT_HEADER_FN(vars);
    expect(result).toBe(
      "This file is generated by ghagen from ci/workflows.ts.\nDo not edit manually.",
    );
  });
});

describe("buildHeaderVariables()", () => {
  it("falls back to <unknown>/0 when location is null", () => {
    const vars = buildHeaderVariables(null);
    expect(vars.source_file).toBe("<unknown>");
    expect(vars.source_line).toBe("0");
    expect(vars.tool).toBe("ghagen");
    expect(vars.version).toMatch(/^\d+\.\d+\.\d+$/);
  });
});

describe("HEADER_VARIABLES", () => {
  it("documents every variable used by the default header", () => {
    expect(HEADER_VARIABLES).toHaveProperty("source_file");
    expect(HEADER_VARIABLES).toHaveProperty("source_line");
    expect(HEADER_VARIABLES).toHaveProperty("tool");
    expect(HEADER_VARIABLES).toHaveProperty("version");
  });
});
