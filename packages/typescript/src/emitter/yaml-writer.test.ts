import { describe, it, expect } from "vitest";
import { mkdtempSync, readFileSync, rmSync } from "node:fs";
import { join } from "node:path";
import { tmpdir } from "node:os";
import { YAMLMap, Scalar, Pair } from "yaml";
import { toYaml, toYamlFile, modelToYamlMap } from "./yaml-writer.js";
import { createModel, raw, withComment, withEolComment } from "../models/_base.js";
import { JOB_KEY_ORDER } from "./key-order.js";

// ---------------------------------------------------------------------------
// helpers
// ---------------------------------------------------------------------------
function simpleModel(
  data: Record<string, unknown> = {},
  meta: Record<string, unknown> = {},
  keyOrder: readonly string[] = [],
) {
  return createModel("test", data, meta, keyOrder);
}

// ---------------------------------------------------------------------------
// toYaml basics
// ---------------------------------------------------------------------------
describe("toYaml()", () => {
  it("produces YAML with default header comment", () => {
    const m = simpleModel({ name: "ci" });
    const yaml = toYaml(m);
    expect(yaml).toContain("# This file is generated by ghagen");
    expect(yaml).toContain("# Do not edit manually.");
  });

  it("omits header when includeHeader is false", () => {
    const m = simpleModel({ name: "ci" });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).not.toContain("This file is generated by ghagen");
  });

  it("uses a custom header string", () => {
    const m = simpleModel({ name: "ci" });
    const yaml = toYaml(m, { header: "Custom header" });
    expect(yaml).toContain("# Custom header");
    expect(yaml).not.toContain("Do not edit manually");
  });

  it("falls back to default header when header is null", () => {
    const m = simpleModel({ name: "ci" });
    const yaml = toYaml(m, { header: null });
    expect(yaml).toContain("# This file is generated by ghagen");
  });
});

// ---------------------------------------------------------------------------
// modelToYamlMap / key ordering
// ---------------------------------------------------------------------------
describe("modelToYamlMap() key ordering", () => {
  it("orders keys according to keyOrder, then remaining in insertion order", () => {
    const m = createModel(
      "job",
      { steps: [], name: "build", "runs-on": "ubuntu-latest", env: {} },
      {},
      JOB_KEY_ORDER,
    );
    const map = modelToYamlMap(m);
    const keys = map.items.map((p: Pair) => (p.key instanceof Scalar ? p.key.value : p.key));
    // JOB_KEY_ORDER puts name, runs-on, ... env, ... steps
    expect(keys.indexOf("name")).toBeLessThan(keys.indexOf("runs-on"));
    expect(keys.indexOf("runs-on")).toBeLessThan(keys.indexOf("steps"));
  });

  it("merges extras after schema fields", () => {
    const m = createModel("test", { name: "ci" }, { extras: { "x-custom": true } }, ["name"]);
    const map = modelToYamlMap(m);
    const keys = map.items.map((p: Pair) => (p.key instanceof Scalar ? p.key.value : p.key));
    expect(keys).toEqual(["name", "x-custom"]);
  });
});

// ---------------------------------------------------------------------------
// Comments
// ---------------------------------------------------------------------------
describe("comments", () => {
  it("withComment adds a block comment before the field", () => {
    const m = simpleModel({ name: withComment("ci", "The workflow name") });
    const yaml = toYaml(m, { includeHeader: false });
    const lines = yaml.split("\n");
    const commentIdx = lines.findIndex((l) => l.includes("# The workflow name"));
    const fieldIdx = lines.findIndex((l) => l.startsWith("name:"));
    expect(commentIdx).toBeGreaterThanOrEqual(0);
    expect(commentIdx).toBeLessThan(fieldIdx);
  });

  it("withEolComment adds an EOL comment on the field value", () => {
    const m = simpleModel({ name: withEolComment("ci", "inline note") });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toMatch(/name: ci\s+# inline note/);
  });

  it("model-level comment attaches to the first key", () => {
    const m = simpleModel({ name: "ci", on: "push" }, { comment: "Top comment" });
    const yaml = toYaml(m, { includeHeader: false });
    const lines = yaml.split("\n");
    const commentIdx = lines.findIndex((l) => l.includes("# Top comment"));
    const nameIdx = lines.findIndex((l) => l.startsWith("name:"));
    expect(commentIdx).toBeGreaterThanOrEqual(0);
    expect(commentIdx).toBeLessThan(nameIdx);
  });

  it("model-level eolComment attaches to the last value", () => {
    const m = simpleModel({ alpha: 1, zulu: 2 }, { eolComment: "end of block" });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toMatch(/zulu: 2\s+# end of block/);
  });
});

// ---------------------------------------------------------------------------
// postProcess
// ---------------------------------------------------------------------------
describe("postProcess", () => {
  it("callback can add pairs to the YAMLMap", () => {
    const m = simpleModel(
      { name: "ci" },
      {
        postProcess: (node: YAMLMap) => {
          node.items.push(new Pair(new Scalar("injected"), new Scalar("yes")));
        },
      },
    );
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("injected: yes");
  });
});

// ---------------------------------------------------------------------------
// Value conversion
// ---------------------------------------------------------------------------
describe("value conversion", () => {
  it("multiline string ending with newline uses block literal |", () => {
    const m = simpleModel({ script: "echo hello\necho world\n" });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("script: |\n");
  });

  it("multiline string without trailing newline uses strip |-", () => {
    const m = simpleModel({ script: "echo hello\necho world" });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("script: |-\n");
  });

  it("single-line string is plain (no block indicator)", () => {
    const m = simpleModel({ name: "ci" });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("name: ci");
    expect(yaml).not.toMatch(/name: [|>]/);
  });

  it("raw() values are unwrapped as plain scalars", () => {
    const m = simpleModel({ expr: raw("${{ github.event }}") });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("expr: ${{ github.event }}");
  });

  it("nested model produces a recursive YAMLMap", () => {
    const inner = simpleModel({ image: "node:20" }, {}, ["image"]);
    const outer = simpleModel({ container: inner }, {}, ["container"]);
    const yaml = toYaml(outer, { includeHeader: false });
    expect(yaml).toContain("container:");
    expect(yaml).toContain("  image: node:20");
  });

  it("array produces a YAML sequence", () => {
    const m = simpleModel({ items: ["a", "b", "c"] });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("- a");
    expect(yaml).toContain("- b");
    expect(yaml).toContain("- c");
  });

  it("plain object produces a nested map", () => {
    const m = simpleModel({ env: { NODE_ENV: "production" } });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("env:");
    expect(yaml).toContain("  NODE_ENV: production");
  });

  it("null value is serialized as a null entry", () => {
    const m = simpleModel({ empty: null });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("? empty");
  });

  it("undefined value is serialized as a null entry", () => {
    const m = simpleModel({ empty: undefined });
    const yaml = toYaml(m, { includeHeader: false });
    expect(yaml).toContain("? empty");
  });
});

// ---------------------------------------------------------------------------
// toYamlFile
// ---------------------------------------------------------------------------
describe("toYamlFile()", () => {
  it("writes YAML to disk matching toYaml() output", () => {
    const dir = mkdtempSync(join(tmpdir(), "ghagen-test-"));
    try {
      const m = simpleModel({ name: "ci" });
      const filePath = join(dir, "workflow.yml");
      toYamlFile(m, filePath);
      const contents = readFileSync(filePath, "utf-8");
      expect(contents).toBe(toYaml(m));
    } finally {
      rmSync(dir, { recursive: true, force: true });
    }
  });
});
